/* SDK Auth API Reference
 * 
   *** init ***
    Initializes the Cloud Services Platform (JavaScript) library by obtaining the OAuth application authorization token.
   *** login
    Authenticates users with the Intel® Identity Services using an Intel or social account.
   *** logout
    Logs the user out of the Intel Identity Services.
   *** getAuthStatus
    Retrieves the current token object.
   *** getApplicationInstance
    Allows the application to obtain the application instance ID.
   *** getTokenInfo
    Obtains detailed information about a current token.
   *** setAccessToken
    Sets a specific access token instead of retrieving it using the intel.auth.init or intel.auth.login methods.
   *** Access Token object
    Returned when the init and login methods are executed successfully.

  See more at: http://software.intel.com/cloudservicesplatform/documentation/intel-cloud-services-sdk-beta-auth-api-reference
